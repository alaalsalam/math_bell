const LAST_INDEX = {};

function pickNoImmediateRepeat(type, pool) {
  if (!Array.isArray(pool) || pool.length === 0) return "أنت بطل!";
  if (pool.length === 1) return pool[0];

  const previous = Number.isInteger(LAST_INDEX[type]) ? LAST_INDEX[type] : -1;
  let next = Math.floor(Math.random() * pool.length);
  if (next === previous) {
    next = (next + 1 + Math.floor(Math.random() * (pool.length - 1))) % pool.length;
  }
  LAST_INDEX[type] = next;
  return pool[next] || pool[0];
}

export function getSaudiMessage(type = "correct") {
  const pool = SAUDI_MESSAGES[type] || SAUDI_MESSAGES.correct;
  return pickNoImmediateRepeat(type, pool);
}
